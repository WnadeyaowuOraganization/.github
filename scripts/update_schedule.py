def update_schedule():
    with open('docs/SCHEDULE.md', 'r') as f:
        content = f.read()

    updated_count = 0

    # Mark completed issues
    for issue_num in completed_issues:
        pattern = rf'(\| #{issue_num} \|.*?\|) 待执行 (\|)'
        replacement = r'\1 已完成 \2'
        new_content = re.sub(pattern, replacement, content)
        if new_content != content:
            content = new_content
            updated_count += 1
            print(f"Marked #{issue_num} as 已完成")

    # Mark failed issues
    for issue_num in failed_issues:
        pattern = rf'(\| #{issue_num} \|.*?\|) 待执行 (\|)'
        replacement = r'\1 失败 \2'
        new_content = re.sub(pattern, replacement, content)
        if new_content != content:
            content = new_content
            updated_count += 1
            print(f"Marked #{issue_num} as 失败")

    # Update timestamp
    content = re.sub(
        r'\*\*更新时间\*\*: \d{4}-\d{2}-\d{2}',
        '**更新时间**: 2026-03-28',
        content
    )

    with open('docs/SCHEDULE.md', 'w') as f:
        f.write(content)

    print(f"\nTotal updated: {updated_count} issues")
    print(f"Completed: {len(completed_issues)}")
    print(f"Failed: {len(failed_issues)}")

if __name__ == '__main__':
    update_schedule()
